candidate = input("Enter a electoral candiate, the options are: A)Red, B)Green or C)Blue: ")

if candidate == 'a' or candidate ==  'A':
    print("U vote for Red candidate")
elif candidate == 'b' or candidate == 'B':
    print("U vote for Green candidate")
elif candidate == 'c' or candidate == 'C':
    print("U vote for Blue candidate")
else:
    print("Invalid input")